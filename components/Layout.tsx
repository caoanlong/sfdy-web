import React, { ReactNode } from 'react'
import Head from 'next/head'
import HeaderBar from './HeaderBar'
import FooterBar from './FooterBar'
import VodType from '../types/VodType'

function decodeUnicode(str: string) {
    str = str.replace(/\\/g, "%")
    return unescape(str)
}

export const getVodTypes = async () => {
    const res = await fetch('https://jyavs.com/api/type/findAll')
    const data = await res.json()
    if (!data || data.code != 200 || !data.data) {
      return null
    }
    return data.data.map((item: VodType) => ({ ...item, typeExtend: JSON.parse(decodeUnicode(item.typeExtend)) }))
}

type LayoutProps = {
    children: ReactNode,
    vodTypes: Array<VodType>
}

function Layout({children, vodTypes}: LayoutProps) {
    return (
        <div className="bg-gray-50">
            <Head>
                <title>巨硬AV</title>
                <meta name="description" content="Generated by create next app" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <HeaderBar vodTypes={vodTypes}></HeaderBar>
            <div>{children}</div>
            <FooterBar></FooterBar>
        </div>
    )
}

export default Layout